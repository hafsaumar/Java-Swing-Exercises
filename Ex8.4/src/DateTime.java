    public void updateLabels() {
        // Present the texts using the current locale,
        // String from the resource bundle, numbers formatted
        String time = timeFormat.format(new Date());
        dateLabel.setText(dateFormat.format(new Date()));
       // String time = dateFormat.format(new Date());
        timeLabel.setText(time);
    }

    /**
     * This method updates the objects used to present information.
     */
    public void updateLocaleSpecifics(Locale currentLocale) {
        dateFormat = DateFormat.getDateInstance(DateFormat.SHORT, currentLocale);
        timeFormat = DateFormat.getTimeInstance(DateFormat.SHORT, currentLocale);
    }

