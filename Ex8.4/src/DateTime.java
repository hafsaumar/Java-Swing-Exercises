    private void setComboBox() {
        ///combobox settings
        Locale[] locales = Locale.getAvailableLocales();
        localeCombobox = new JComboBox(locales);
        for (int i = 0;i<locales.length;i++){
            localeCombobox.addItem(locales[i].toString());
        }
        localeCombobox.setRenderer(new LocaleRenderer(Locale.US));
        localeCombobox.addActionListener(new ActionListener() {
            public void actionPerformed(ActionEvent e) {
                Locale l = (Locale) localeCombobox.getSelectedItem();
                updateLocaleSpecifics(l);
                updateLabels();
                localeCombobox.setRenderer(new LocaleRenderer(l));
            }
        });
        localeCombobox.repaint();
    }

    private void setSystemTray() {
        ////Seting System tray
        if (SystemTray.isSupported()) {
            // System tray is there already, get access to related object
            SystemTray tray = SystemTray.getSystemTray();
            try {
                // Tray contains icons, which are TrayIcon objects
                tray.add(new WeekTrayIcon(this));
            } catch (AWTException ex) {
            }
        }
        setDefaultCloseOperation(HIDE_ON_CLOSE);
        pack();
        setVisible(true);
    }

